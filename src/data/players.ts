// compute average finish position from last5 strings
export function avgPosition(last5: string[]): number {
  const nums = last5.map((r) => {
    if (!r || r === "MC" || r === "WD" || r === "DQ") return 70;
    const n = parseInt(r.replace(/^T/, ""), 10);
    return Number.isFinite(n) ? n : 70;
  });
  if (nums.length === 0) return 70;
  return Math.round(nums.reduce((a, b) => a + b, 0) / nums.length);
}
